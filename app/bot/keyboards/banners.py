BANNERS_DIR = STATIC_DIR / "banners"


async def get_img(
    menu_name: str,
    file_dir: str = BANNERS_DIR,
    caption: str = None,
    f_type: str = FMT_JPG,
) -> InputMediaPhoto:
    """
    –ü–æ–ª—É—á–∏—Ç—å –∏–∑–æ–±—Ä–∞–∂–µ–Ω–∏–µ.
    –í —Ñ—É–Ω–∫—Ü–∏—é –ø–µ—Ä–µ–¥–∞—é—Ç—Å—è –∏–º—è –º–µ–Ω—é, —É—Ä–æ–≤–µ–Ω—å –º–µ–Ω—é –∏ –æ–ø–∏—Å–∞–Ω–∏–µ,
    –µ—Å–ª–∏ –æ–ø–∏—Å–∞–Ω–∏—è –Ω–µ—Ç, —Ç–æ –æ–Ω–æ –±–µ—Ä–µ—Ç—Å—è –∏–∑ –∫–ª–∞—Å—Å–∞ Captions.
    –ï—Å–ª–∏ –Ω–µ—Ç –Ω–µ–æ–±—Ö–æ–¥–∏–º–æ–≥–æ –∏–∑–æ–±—Ä–∞–∂–µ–Ω–∏—è, —Ç–æ –±–µ—Ä–µ—Ç—Å—è –∫–∞—Ä—Ç–∏–Ω–∫–∞ 'no_image'.
    """
    if not caption:
        caption = getattr(captions, menu_name)
    media = await get_file(menu_name, file_dir, f_type)
    return InputMediaPhoto(media=media, caption=caption)


async def get_file(
    filename: str, file_dir: str = BANNERS_DIR, f_type: str = FMT_JPG
) -> FSInputFile:
    """–ü–æ–ª—É—á–∏—Ç—å —Ñ–∞–π–ª –ø–æ –∏–º–µ–Ω–∏ –≤ –∑–∞–¥–∞–Ω–Ω–æ–π –¥–∏—Ä–µ–∫—Ç–æ—Ä–∏–∏."""
    if await is_file_in_dir(filename + f_type, file_dir):
        return FSInputFile(file_dir.joinpath(filename + f_type))
    return FSInputFile(file_dir.joinpath(NO_IMAGE + FMT_JPG))


async def get_qr_code_image(client_id: str, encoded_value: str):
    """
    –°–æ–∑–¥–∞—Ç—å qr –∫–æ–¥.
    –í–æ–∑–≤—Ä–∞—â–∞–µ—Ç InputMediaPhoto c –∏–∑–æ–±—Ä–∞–∂–µ–Ω–∏–µ–º –∏ –æ–ø–∏—Å–∞–Ω–∏–µ–º.
    """
    value = await decode_data(encoded_value)
    qr_data = "_".join((client_id, value))
    logger(qr_data)
    qr_img = segno.make_qr(qr_data)
    file_name = QR_DIR / ((await generate_filename()) + FMT_PNG)
    logger(file_name)
    qr_img.save(file_name, scale=10)
    mediaphoto = InputMediaPhoto(media=FSInputFile(file_name), caption=captions.confirm_trade)
    # return BufferedInputFile(buffer.getvalue(), filename=f"QR {client_id}")
    # await delete_file(file_name)
    return mediaphoto


